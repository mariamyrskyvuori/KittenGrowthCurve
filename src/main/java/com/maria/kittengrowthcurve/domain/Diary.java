package com.maria.kittengrowthcurve.domain;

import java.time.LocalDate;

/**
 *
 * @author maria
 */
public class Diary {
    private int id;
    private LocalDate date;
    private String text;

    public Diary(int id, LocalDate date, String text) {
        this.id = id;
        this.date = date;
        this.text = text;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
